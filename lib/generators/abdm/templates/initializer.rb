# Configuration for ABDM integration
#
# You need to set the client_id, client_secret, environment, hpr_id, and password.
# By default, the environment is set to 'production'. You can change it to 'sandbox'.
#
# The hpr_id and password are used to automatically generate the hpr_token
# required for certain API requests, so you don't need to manually handle token generation.
#
# Example:
# ABDMConfig = {
#   environment: 'sandbox', # or 'production'
#   client_id: 'your_client_id',
#   client_secret: 'your_client_secret',
#
#   hpr_id: 'your_hpr_id Ex: 71-2123-1232-4354',
#   password: 'your_password'
# }
